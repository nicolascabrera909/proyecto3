class Armament {


    /*Constructor*/
    constructor(power, distance) {
        this.power = power;
        this.distance = distance;
    }

    /*Geters and seters*/
    getPower() {
        return this.power;
    }
    getDistance() {
        return this.distance;
    }
    setPower(poder) {
        this.power = poder;
    }
    setDistance(metros) {
        this.distance = metro;
    }

    


}
export default Armament;
