class Map {

    /*Constructor*/
    constructor(height, width) {
        this.height = height;
        this.width = width;

    }

    /*Geters and seters*/
    getHeight() {
        return this.height
    }
    getWidth() {
        return this.width
    }
    setHeight(height) {
         this.height=height;
    }
    setWidth(width) {
         this.width=width;
    }


}
